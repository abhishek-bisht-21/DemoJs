
console.log("Operators in JS");
let a = 45;
let b = 4;
console.log("a + b: ", a+b);
console.log("a - b: ", a-b);
console.log("a * b: ", a*b);
console.log("a / b: ", a/b);
console.log("a % b: ", a%b);
console.log("a ** b: ", a**b);

console.log("a++ :", a++);
console.log("After Incrementing a: ", a);
console.log("a--: ", a--);
console.log("After Decrementing b: ", b);

console.log("Pre Increment ++b: ", ++b)
console.log("Pre Decrement ++b: ", --b)


// Assignment Operator
let x = 10
console.log("x+=10 : ",x+=10); // x = 20
console.log("x-=10 : ",x-=10); // x = 10
console.log("x*=10 : ",x*=10); // x = 100
console.log("x/=10 : ",x/=10); // x = 10


// Comparison Operator
let comp1 = 10
let comp2 = 20
let comp3 = "10"

console.log("comp1,comp2,comp3", comp1,comp2,comp3)
console.log("comp1 == comp2 is ", comp1 == comp2);
console.log("comp1 != comp2 is ", comp1 != comp2);
console.log("comp1 == comp3 is ", comp1 == comp3);
console.log("comp1 === comp3 is ", comp1 === comp3);
console.log("comp1 != comp3 is ", comp1 != comp3);
console.log("comp1 !== comp3 is ", comp1 !== comp3);














