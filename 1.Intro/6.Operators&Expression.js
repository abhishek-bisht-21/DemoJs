
console.log("Operators in JS");
let a = 45;
let b = 4;
console.log("a + b: ", a+b);
console.log("a - b: ", a-b);
console.log("a * b: ", a*b);
console.log("a / b: ", a/b);
console.log("a % b: ", a%b);
console.log("a ** b: ", a**b);

console.log("a++ :", a++);
console.log("After Incrementing a: ", a);
console.log("a--: ", a--);
console.log("After Decrementing b: ", b);

console.log("Pre Increment ++b: ", ++b)
console.log("Pre Decrement ++b: ", --b)







